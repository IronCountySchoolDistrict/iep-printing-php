    <h2 id="print-forms">Print Forms</h2>
    <p>
      Select the IEP you want to print forms for. Then click the checkboxes next to each form you would like to print. Then click the blue <i>Print Selected</i> button.
    </p>
    <p>
      The default printing options are set to Concatenation and Final (no watermark).
    </p>
    <p>
      Printing options:
      <ul>
        <li>File:
          <ul>
            <li>Concatenate - Each form selected will combine into one printable PDF.</li>
            <li>Zip - Each form selected will be its own file archived and compressed into a zip file.</li>
          </ul>
        </li>
        <li>Watermark
          <ul>
            <li>Final - No watermark is added to the pdf.</li>
            <li>Draft - A large watermark with the word "DRAFT" will be printed on each page in the resulting PDFs.</li>
            <li>Copy - A large watermark with the word "COPY" will be printed on each page in the resulting PDFs.</li>
          </ul>
        </li>
      </ul>
    </p>
