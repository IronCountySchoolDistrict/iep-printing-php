<div class="row">
  <h2 id="activate-iep">Make an IEP active</h2>
  <p>
    In order to activate an IEP, the IEP can never have been active in the past. If an IEP can be activated a link will appear above the IEP status badge.
  </p>
  <p>
    Click the <i>Activate this IEP</i> link and a warning will pop up. If you're okay with the consequences click <i>OK</i>.
  </p>
  <p>
    Now your IEP will have the Active status badge. Note that the only way to deactivate an IEP is to activate another or wait until the IEP expires.
  </p>
