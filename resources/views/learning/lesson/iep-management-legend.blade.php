    <h2 id="iep-management-legend">IEP Management page explained</h2>
    <ul>
      <li><i>Red</i> - The date that the IEP is started. Is tied to the Meeting date on SpEd 6a1 (IEP form) and so can be changed according to what that field says. Clicking the link will take you to SpEd 6a1.</li>
      <li><i>Orange</i> - The Case Manager for the duration of this IEP. This is tied to SpEd teacher field in form SpEd 51 (Demographic form). Clicking the link will take you to SpEd 51.</li>
      <li><i>Green</i> - IEP status badge. Can be blue for Active, orange for Inactive, and gray for Expired.</li>
      <li><i>Purple box</i> - The IEP that is selected. If there are more than one IEP for a student another IEP could appear in the list. You can tell which IEP is currently selected by the Start Date and CaseManager links being active and underlined.</li>
      <li><i>Blue</i> - Click the New button to create a New IEP.</li>
      <li><i>Pink</i> - Click the trash button to delete the currently selected IEP. Note that you can only delete IEPs that do not have any responses.</li>
      <li><i>Black</i> - This area changed based on the IEP you have selected. Contains links to forms for the selected IEP and has the ability to print forms.</li>
    </ul>
